package nl.avasten.H3.loops;

public class Loops {

    public void forLoopWithIPlusPlus() {

        for (int i = 1; i < 10; i++) {
            System.out.println("Rondje: " + i);
        }
    }

    public void forLoopWithPlusPlusI() {

        for (int i = 1; i < 10; ++i) {
            System.out.println("Rondje: " + i);
        }
    }

}
